int		ft_find_next_prime(iint nb)
{
	int i;

	if (nb == 0 || nb == 1)
		return 2;
	i = 2;
	while (i <= nb / 2)
	{
		if (nb % i++ == 0)
			return nb++;
	}
	return nb;
}
